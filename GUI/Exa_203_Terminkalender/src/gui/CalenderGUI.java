    private void onGenerateDaysOfMonth(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onGenerateDaysOfMonth
        btCreateT.setEnabled(true);
        try{
            String input = tfMonat.getText().trim();
            
            if(input.isEmpty()){
                JOptionPane.showMessageDialog(this, "Sie haben nichts eingegeben");
            }
            String first = input.substring(0, 1).toUpperCase();
            String last = input.substring(1);
            input = first + last;
            
            String output = CalenderBL.generateDaysOfMonth(input);
            epOut.setText(output);
        }catch(DateTimeParseException e){
            JOptionPane.showMessageDialog(this, "Sie haben einen falschen Monat eingegeben");
        }
        
    }//GEN-LAST:event_onGenerateDaysOfMonth

    private void onGenerateEvents(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onGenerateEvents
        String text = epOut.getText();
        btCreateT.setEnabled(false);
        epOut.setText(CalenderBL.generateEvents("feb", text));
    }//GEN-LAST:event_onGenerateEvents

    private void onRemoveEvents(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onRemoveEvents
        try {
            int day = Integer.parseInt(JOptionPane.showInputDialog("Gib einen Tag ein"));
            epOut.setText(CalenderBL.removeEvent(day, epOut.getText()));
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Du hast keinen richtigen Tag eingegeben");
        }
    }//GEN-LAST:event_onRemoveEvents

