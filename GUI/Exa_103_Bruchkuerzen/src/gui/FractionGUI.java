    private void onCalculate(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onCalculate
        int num = Integer.parseInt(tfNumInp.getText());
        int den = Integer.parseInt(tfDenInp.getText());
        Fraction fr = new Fraction(num, den);
        fr.cancel();
        int numOut = fr.getNumerator();
        int denOut = fr.getDenominator();
        tfNumOut.setText(String.format("%d", numOut));
        tfDenOut.setText(String.format("%d", denOut));
    }//GEN-LAST:event_onCalculate

